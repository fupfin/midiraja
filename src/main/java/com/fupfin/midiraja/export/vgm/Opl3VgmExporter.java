    /**
     * Maps MIDI percussion note (0-127) to drum patch index (0-4), or -1 for unmapped.
     */
    private static final int[] DRUM_PATCH_IDX = buildDrumPatchIdx();

    private static int[] buildDrumPatchIdx()
    {
        int[] idx = new int[128];
        java.util.Arrays.fill(idx, -1);
        // Kick
        for (int n : new int[] { 35, 36 })
            idx[n] = 0;
        // Snare
        for (int n : new int[] { 38, 40 })
            idx[n] = 1;
        // Hi-hat
        for (int n : new int[] { 42, 44, 46 })
            idx[n] = 2;
        // Tom
        for (int n : new int[] { 41, 43, 45, 47, 48, 50 })
            idx[n] = 3;
        // Cymbal
        for (int n : new int[] { 49, 51, 53, 55, 57, 59 })
            idx[n] = 4;
        return idx;
    }

    public void export(Sequence sequence, OutputStream out)
    {
        try (var writer = new VgmWriter(out, VgmWriter.ChipMode.OPL3))
        {
            var events = mergeAndSort(sequence);
            var state = new Opl3State();
            state.initSilence(writer);

            long prevTick = 0;
            double ticksPerSample = ticksPerSample(sequence);

            for (var event : events)
            {
                long tick = event.getTick();
                int waitSamples = (int) Math.round((tick - prevTick) / ticksPerSample);
                if (waitSamples > 0)
                    writer.waitSamples(waitSamples);
                prevTick = tick;

                if (event.getMessage() instanceof ShortMessage msg)
                    state.handleMessage(msg, writer);
            }
        }
    }

    private static double ticksPerSample(Sequence sequence)
    {
        int resolution = sequence.getResolution();
        double ticksPerSecond = resolution * 1_000_000.0 / 500_000.0;
        return ticksPerSecond / VgmWriter.VGM_SAMPLE_RATE;
    }

    private static List<MidiEvent> mergeAndSort(Sequence sequence)
    {
        var events = new ArrayList<MidiEvent>();
        for (Track track : sequence.getTracks())
        {
            for (int i = 0; i < track.size(); i++)
                events.add(track.get(i));
        }
        events.sort(Comparator.comparingLong(MidiEvent::getTick));
        return events;
    }

    // ── OPL3 state ────────────────────────────────────────────────────────────

    private final class Opl3State
    {
        private final int[] noteSlot = new int[TOTAL_SLOTS];
        private final int[] chanSlot = new int[TOTAL_SLOTS];
        private final boolean[] active = new boolean[TOTAL_SLOTS];
        private final int[] program = new int[16];
        private int drumRoundRobin = 0;

        Opl3State()
        {
            java.util.Arrays.fill(noteSlot, -1);
            java.util.Arrays.fill(chanSlot, -1);
        }

        void initSilence(VgmWriter w)
        {
            // Enable OPL3 mode
            w.writeOpl3Bank1(0x05, 0x01);
            // Key-off all channels
            for (int ch = 0; ch < 9; ch++)
                w.writeOpl3(0xB0 + ch, 0);
            for (int ch = 0; ch < 9; ch++)
                w.writeOpl3Bank1(0xB0 + ch, 0);
        }

        void handleMessage(ShortMessage msg, VgmWriter w)
        {
            int status = msg.getCommand();
            int midiCh = msg.getChannel();
            int d1 = msg.getData1();
            int d2 = msg.getData2();

            switch (status)
            {
                case ShortMessage.PROGRAM_CHANGE -> program[midiCh] = d1;
                case ShortMessage.NOTE_ON ->
                {
                    if (d2 > 0)
                        noteOn(midiCh, d1, d2, w);
                    else
                        noteOff(midiCh, d1, w);
                }
                case ShortMessage.NOTE_OFF -> noteOff(midiCh, d1, w);
            }
        }

        private void noteOn(int midiCh, int note, int velocity, VgmWriter w)
        {
            boolean isPercussion = (midiCh == 9);

            int slot;
            if (isPercussion)
            {
                slot = MELODIC_SLOTS + drumRoundRobin;
                drumRoundRobin = (drumRoundRobin + 1) % DRUM_SLOTS;
            }
            else
            {
                slot = findFreeSlot();
                if (slot < 0)
                    slot = stealMelodicSlot();
            }

            if (active[slot])
                keyOff(slot, w);

            int[] patch = isPercussion ? drumPatch(note) : melodicPatch(midiCh);
            writePatch(slot, patch, velocity, w);
            writeFreqKeyOn(slot, note, w);

            noteSlot[slot] = note;
            chanSlot[slot] = midiCh;
            active[slot] = true;
        }

        private void noteOff(int midiCh, int note, VgmWriter w)
        {
            for (int slot = 0; slot < TOTAL_SLOTS; slot++)
            {
                if (active[slot] && noteSlot[slot] == note && chanSlot[slot] == midiCh)
                {
                    keyOff(slot, w);
                    active[slot] = false;
                    noteSlot[slot] = -1;
                    chanSlot[slot] = -1;
                    return;
                }
            }
        }

        private void keyOff(int slot, VgmWriter w)
        {
            int bankSlot = bankAndChannelIndex(slot);
            int bank = bankSlot >> 8;
            int ch = bankSlot & 0xFF;
            if (bank == 0)
                w.writeOpl3(0xB0 + ch, 0);
            else
                w.writeOpl3Bank1(0xB0 + ch, 0);
        }

        private void writePatch(int slot, int[] patch, int velocity, VgmWriter w)
        {
            int bankSlot = bankAndChannelIndex(slot);
            int bank = bankSlot >> 8;
            int ch = bankSlot & 0xFF;
            int modOff = CH_TO_MOD_OFF[ch];
            int carOff = CH_TO_CAR_OFF[ch];

            // Modulator params
            int modAVEKM = patch[0];
            int modKSLTL = patch[1];
            int modARDR = patch[2];
            int modSLRR = patch[3];
            int modWS = patch[4];
            // Carrier params
            int carAVEKM = patch[5];
            int carKSLTL = (patch[6] & 0xC0) | ((127 - velocity) * 40 / 127);
            int carARDR = patch[7];
            int carSLRR = patch[8];
            int carWS = patch[9];
            int fbcnt = patch[10];

            if (bank == 0)
            {
                w.writeOpl3(0x20 + modOff, modAVEKM);
                w.writeOpl3(0x40 + modOff, modKSLTL);
                w.writeOpl3(0x60 + modOff, modARDR);
                w.writeOpl3(0x80 + modOff, modSLRR);
                w.writeOpl3(0xE0 + modOff, modWS);
                w.writeOpl3(0x20 + carOff, carAVEKM);
                w.writeOpl3(0x40 + carOff, carKSLTL);
                w.writeOpl3(0x60 + carOff, carARDR);
                w.writeOpl3(0x80 + carOff, carSLRR);
                w.writeOpl3(0xE0 + carOff, carWS);
                w.writeOpl3(0xC0 + ch, fbcnt | 0x30); // FB/CNT + L+R output
            }
            else
            {
                w.writeOpl3Bank1(0x20 + modOff, modAVEKM);
                w.writeOpl3Bank1(0x40 + modOff, modKSLTL);
                w.writeOpl3Bank1(0x60 + modOff, modARDR);
                w.writeOpl3Bank1(0x80 + modOff, modSLRR);
                w.writeOpl3Bank1(0xE0 + modOff, modWS);
                w.writeOpl3Bank1(0x20 + carOff, carAVEKM);
                w.writeOpl3Bank1(0x40 + carOff, carKSLTL);
                w.writeOpl3Bank1(0x60 + carOff, carARDR);
                w.writeOpl3Bank1(0x80 + carOff, carSLRR);
                w.writeOpl3Bank1(0xE0 + carOff, carWS);
                w.writeOpl3Bank1(0xC0 + ch, fbcnt | 0x30);
            }
        }

        private void writeFreqKeyOn(int slot, int note, VgmWriter w)
        {
            double freq = 440.0 * Math.pow(2.0, (note - 69) / 12.0);
            int block = 0;
            while (block < 7 && freq >= 261.63 * Math.pow(2.0, block))
                block++;
            if (block > 0)
                block--;
            int fnum = (int) Math.round(freq * (1L << (20 - block)) / 49716.0);
            fnum = Math.clamp(fnum, 0, 0x3FF);

            int bankSlot = bankAndChannelIndex(slot);
            int bank = bankSlot >> 8;
            int ch = bankSlot & 0xFF;

            if (bank == 0)
            {
                w.writeOpl3(0xA0 + ch, fnum & 0xFF);
                w.writeOpl3(0xB0 + ch, 0x20 | (block << 2) | ((fnum >> 8) & 0x03)); // key on
            }
            else
            {
                w.writeOpl3Bank1(0xA0 + ch, fnum & 0xFF);
                w.writeOpl3Bank1(0xB0 + ch, 0x20 | (block << 2) | ((fnum >> 8) & 0x03));
            }
        }

        /**
         * Returns the bank (upper byte) and OPL3 channel index (lower byte) for a slot.
         * Slots 0-8 → bank 0 channels 0-8; slots 9-13 → bank 1 channels 0-4;
         * slots 14-17 → drum round-robin in bank 0 channels 5-8.
         */
        private static int bankAndChannelIndex(int slot)
        {
            if (slot < 9)
                return slot; // bank 0, ch 0-8
            if (slot < MELODIC_SLOTS)
                return (1 << 8) | (slot - 9); // bank 1, ch 0-4
            return (slot - MELODIC_SLOTS + 5); // bank 0, ch 5-8 for drums
        }

        private int findFreeSlot()
        {
            for (int i = 0; i < MELODIC_SLOTS; i++)
            {
                if (!active[i])
                    return i;
            }
            return -1;
        }

        private int stealMelodicSlot()
        {
            for (int i = MELODIC_SLOTS - 1; i >= 0; i--)
            {
                if (active[i])
                    return i;
            }
            return 0;
        }

        private int[] melodicPatch(int midiCh)
        {
            int prog = program[midiCh];
            if (prog < 0 || prog >= GM_PATCHES.length)
                prog = 0;
            return GM_PATCHES[prog];
        }

        private static int[] drumPatch(int note)
        {
            int idx = (note >= 0 && note < DRUM_PATCH_IDX.length) ? DRUM_PATCH_IDX[note] : -1;
            return (idx >= 0) ? DRUM_PATCHES[idx] : DRUM_PATCHES[0];
        }
    }
